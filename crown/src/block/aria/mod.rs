/// Diffusion layer A (involution).
#[inline]
fn diffusion_a(x: &[u8; 16]) -> [u8; 16] {
    let mut y = [0u8; 16];
    y[0] = x[3] ^ x[4] ^ x[6] ^ x[8] ^ x[9] ^ x[13] ^ x[14];
    y[1] = x[2] ^ x[5] ^ x[7] ^ x[8] ^ x[9] ^ x[12] ^ x[15];
    y[2] = x[1] ^ x[4] ^ x[6] ^ x[10] ^ x[11] ^ x[12] ^ x[15];
    y[3] = x[0] ^ x[5] ^ x[7] ^ x[10] ^ x[11] ^ x[13] ^ x[14];
    y[4] = x[0] ^ x[2] ^ x[5] ^ x[8] ^ x[11] ^ x[14] ^ x[15];
    y[5] = x[1] ^ x[3] ^ x[4] ^ x[9] ^ x[10] ^ x[14] ^ x[15];
    y[6] = x[0] ^ x[2] ^ x[7] ^ x[9] ^ x[10] ^ x[12] ^ x[13];
    y[7] = x[1] ^ x[3] ^ x[6] ^ x[8] ^ x[11] ^ x[12] ^ x[13];
    y[8] = x[0] ^ x[1] ^ x[4] ^ x[7] ^ x[10] ^ x[13] ^ x[15];
    y[9] = x[0] ^ x[1] ^ x[5] ^ x[6] ^ x[11] ^ x[12] ^ x[14];
    y[10] = x[2] ^ x[3] ^ x[5] ^ x[6] ^ x[8] ^ x[13] ^ x[15];
    y[11] = x[2] ^ x[3] ^ x[4] ^ x[7] ^ x[9] ^ x[12] ^ x[14];
    y[12] = x[1] ^ x[2] ^ x[6] ^ x[7] ^ x[9] ^ x[11] ^ x[12];
    y[13] = x[0] ^ x[3] ^ x[6] ^ x[7] ^ x[8] ^ x[10] ^ x[13];
    y[14] = x[0] ^ x[3] ^ x[4] ^ x[5] ^ x[9] ^ x[11] ^ x[14];
    y[15] = x[1] ^ x[2] ^ x[4] ^ x[5] ^ x[8] ^ x[10] ^ x[15];
    y
}

/// Round function FO: FO(D, RK) = A(SL1(D ^ RK))
#[inline]
fn fo(d: &[u8; 16], rk: &[u8; 16]) -> [u8; 16] {
    let mut t = xor_blocks(d, rk);
    sl1(&mut t);
    diffusion_a(&t)
}

/// Round function FE: FE(D, RK) = A(SL2(D ^ RK))
#[inline]
fn fe(d: &[u8; 16], rk: &[u8; 16]) -> [u8; 16] {
    let mut t = xor_blocks(d, rk);
    sl2(&mut t);
    diffusion_a(&t)
}

impl Aria {
    /// Create a new ARIA cipher instance.
    ///
    /// Key must be 16, 24, or 32 bytes for ARIA-128, ARIA-192, or ARIA-256.
    pub fn new(key: &[u8]) -> CryptoResult<Self> {
        let rounds = match key.len() {
            16 => 12,
            24 => 14,
            32 => 16,
            _ => {
                return Err(CryptoError::InvalidKeySize {
                    expected: "16, 24, or 32",
                    actual: key.len(),
                });
            }
        };

        // Select CK1, CK2, CK3 based on key size
        let (ck1, ck2, ck3) = match key.len() {
            16 => (&C1, &C2, &C3),
            24 => (&C2, &C3, &C1),
            32 => (&C3, &C1, &C2),
            _ => unreachable!(),
        };

        // KL || KR = Key padded to 256 bits
        let mut kl = [0u8; 16];
        let mut kr = [0u8; 16];
        if key.len() <= 16 {
            kl[..key.len()].copy_from_slice(key);
        } else if key.len() <= 32 {
            kl.copy_from_slice(&key[..16]);
            let remaining = key.len() - 16;
            kr[..remaining].copy_from_slice(&key[16..]);
        }

        // W0 = KL
        let w0 = kl;
        // W1 = FO(W0, CK1) ^ KR
        let mut w1 = fo(&w0, ck1);
        xor_block(&mut w1, &kr);
        // W2 = FE(W1, CK2) ^ W0
        let mut w2 = fe(&w1, ck2);
        xor_block(&mut w2, &w0);
        // W3 = FO(W2, CK3) ^ W1
        let mut w3 = fo(&w2, ck3);
        xor_block(&mut w3, &w1);

        // Generate all 17 encryption round keys
        let mut ek = [[0u8; 16]; 17];

        // ek1  = W0 ^ (W1 >>> 19)
        ek[0] = xor_blocks(&w0, &rot_right_128(&w1, 19));
        // ek2  = W1 ^ (W2 >>> 19)
        ek[1] = xor_blocks(&w1, &rot_right_128(&w2, 19));
        // ek3  = W2 ^ (W3 >>> 19)
        ek[2] = xor_blocks(&w2, &rot_right_128(&w3, 19));
        // ek4  = (W0 >>> 19) ^ W3
        ek[3] = xor_blocks(&rot_right_128(&w0, 19), &w3);
        // ek5  = W0 ^ (W1 >>> 31)
        ek[4] = xor_blocks(&w0, &rot_right_128(&w1, 31));
        // ek6  = W1 ^ (W2 >>> 31)
        ek[5] = xor_blocks(&w1, &rot_right_128(&w2, 31));
        // ek7  = W2 ^ (W3 >>> 31)
        ek[6] = xor_blocks(&w2, &rot_right_128(&w3, 31));
        // ek8  = (W0 >>> 31) ^ W3
        ek[7] = xor_blocks(&rot_right_128(&w0, 31), &w3);
        // ek9  = W0 ^ (W1 <<< 61)
        ek[8] = xor_blocks(&w0, &rot_left_128(&w1, 61));
        // ek10 = W1 ^ (W2 <<< 61)
        ek[9] = xor_blocks(&w1, &rot_left_128(&w2, 61));
        // ek11 = W2 ^ (W3 <<< 61)
        ek[10] = xor_blocks(&w2, &rot_left_128(&w3, 61));
        // ek12 = (W0 <<< 61) ^ W3
        ek[11] = xor_blocks(&rot_left_128(&w0, 61), &w3);
        // ek13 = W0 ^ (W1 <<< 31)
        ek[12] = xor_blocks(&w0, &rot_left_128(&w1, 31));
        // ek14 = W1 ^ (W2 <<< 31)
        ek[13] = xor_blocks(&w1, &rot_left_128(&w2, 31));
        // ek15 = W2 ^ (W3 <<< 31)
        ek[14] = xor_blocks(&w2, &rot_left_128(&w3, 31));
        // ek16 = (W0 <<< 31) ^ W3
        ek[15] = xor_blocks(&rot_left_128(&w0, 31), &w3);
        // ek17 = W0 ^ (W1 <<< 19)
        ek[16] = xor_blocks(&w0, &rot_left_128(&w1, 19));

        // Generate decryption round keys
        // dk1 = ek_{n+1}, dk2 = A(ek_n), ..., dk_n = A(ek_2), dk_{n+1} = ek_1
        let n = rounds; // number of rounds
        let num_keys = n + 1; // number of round keys needed
        let mut dk = [[0u8; 16]; 17];
        dk[0] = ek[n]; // dk1 = ek_{n+1}
        for i in 1..n {
            dk[i] = diffusion_a(&ek[n - i]); // dk_{i+1} = A(ek_{n+1-i})
        }
        dk[n] = ek[0]; // dk_{n+1} = ek_1

        // Zero out unused keys for cleanliness
        for i in num_keys..17 {
            dk[i] = [0u8; 16];
        }

        Ok(Self { ek, dk, rounds })
    }

    /// Encrypt/decrypt a single block using the given round keys.
    #[inline]
    fn crypt_block(inout: &mut [u8], rk: &[[u8; 16]; 17], rounds: usize) {
        let mut p = [0u8; 16];
        p.copy_from_slice(&inout[..16]);

        // Rounds 1 through n-1
        // Odd rounds use FO, even rounds use FE (1-indexed)
        for i in 0..(rounds - 1) {
            if i % 2 == 0 {
                // odd round (1,3,5,...) -> FO
                p = fo(&p, &rk[i]);
            } else {
                // even round (2,4,6,...) -> FE
                p = fe(&p, &rk[i]);
            }
        }

        // Last round: C = SL2(P_{n-1} ^ ek_n) ^ ek_{n+1}
        // (for odd n, the last round would use SL1 based on the alternating pattern,
        //  but RFC 5794 specifies the final round always uses SL2 since the last
        //  "full" round is n-1 and the final operation is different)
        //
        // Since rounds is always even (12, 14, 16), round n is even-indexed (0-based: rounds-1),
        // which corresponds to an odd 1-indexed round. But the final round is special:
        // it applies the substitution of the would-be next round type but skips diffusion.
        // For even n: round n-1 (0-based) is odd (1-indexed: n is even),
        // so the last full round was FE, and the final operation uses SL1... NO.
        //
        // Let me re-read the spec carefully:
        // The encryption process alternates FO and FE starting with FO for round 1.
        // After rounds 1 to n-1, the final step is:
        //   C = SL_final(P_{n-1} ^ ek_n) ^ ek_{n+1}
        // where SL_final is the substitution that would be used in round n.
        //
        // Round n is even (since n=12,14,16), so FE would be used, which uses SL2.
        // So the final round applies SL2.

        // Apply the final round
        let mut t = xor_blocks(&p, &rk[rounds - 1]);
        sl2(&mut t);
        xor_block(&mut t, &rk[rounds]);

        inout[..16].copy_from_slice(&t);
    }
}

impl BlockCipher for Aria {
    fn block_size(&self) -> usize {
        16
    }

    fn encrypt_block(&self, inout: &mut [u8]) {
        Self::crypt_block(inout, &self.ek, self.rounds);
    }

    fn decrypt_block(&self, inout: &mut [u8]) {
        Self::crypt_block(inout, &self.dk, self.rounds);
    }
}
