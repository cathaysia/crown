/// XOR two 128-bit blocks: dst ^= src
#[inline(always)]
fn xor_block(dst: &mut [u8; 16], src: &[u8; 16]) {
    for i in 0..16 {
        dst[i] ^= src[i];
    }
}

/// XOR two 128-bit blocks into a new block
#[inline(always)]
fn xor_blocks(a: &[u8; 16], b: &[u8; 16]) -> [u8; 16] {
    let mut out = *a;
    xor_block(&mut out, b);
    out
}

/// Right rotation of a 128-bit value by `n` bits (0 < n < 128).
/// Right rotation means bits shift right and wrap around to the left.
#[inline]
fn rot_right_128(x: &[u8; 16], n: usize) -> [u8; 16] {
    debug_assert!(n > 0 && n < 128);
    let byte_shift = n / 8;
    let bit_shift = n % 8;
    let mut out = [0u8; 16];

    if bit_shift == 0 {
        for i in 0..16 {
            out[(i + byte_shift) % 16] = x[i];
        }
    } else {
        for i in 0..16 {
            let lo = x[i] >> bit_shift;
            let hi = x[(i + 16 - 1) % 16] << (8 - bit_shift);
            out[(i + byte_shift) % 16] = hi | lo;
        }
    }
    out
}

/// Left rotation of a 128-bit value by `n` bits (0 < n < 128).
#[inline]
fn rot_left_128(x: &[u8; 16], n: usize) -> [u8; 16] {
    debug_assert!(n > 0 && n < 128);
    rot_right_128(x, 128 - n)
}

