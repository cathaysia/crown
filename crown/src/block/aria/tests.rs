#[test]
fn rfc_5794_appendix_a_vectors() {
    for test in TEST_VECTORS {
        let cipher = Aria::new(test.key).unwrap();
        let mut block = test.plaintext;

        cipher.encrypt_block(&mut block);
        assert_eq!(block, test.ciphertext);

        cipher.decrypt_block(&mut block);
        assert_eq!(block, test.plaintext);
    }
}

#[test]
fn invalid_key_size() {
    assert!(Aria::new(&[0u8; 15]).is_err());
    assert!(Aria::new(&[0u8; 17]).is_err());
    assert!(Aria::new(&[0u8; 33]).is_err());
}
